#include <iostream>
#include <cstring>
using namespace std;

int main()
{
    int n,x_max(10),x_min(1);
    char judge[10] = {0};
    while(cin >> n && n != 0)
    {
        getchar();//?????????????~~~~~
        gets(judge);
        if(strcmp(judge,"too high") == 0 && x_max >= n)
            x_max = n - 1;
        else if(strcmp(judge,"too low") == 0 && x_min <= n)
            x_min = n + 1;
        else if(strcmp(judge,"right on") == 0)
        {
            if(n >= x_min && n <= x_max)
                cout << "Stan may be honest" << endl;
            else
                cout << "Stan is dishonest" << endl;
            x_max = 10,x_min = 0;
        }
    }

    return 0;
}
