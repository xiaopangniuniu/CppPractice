#include <iostream>
#include <cstring>
using namespace std;

char pre[30],in[30];

void recovery(int ps,int pe,int is,int ie)
{
    if(ps+1 >= pe)
        return;
    int i=is,j;
    while(pre[ps] != in[i])
        i++;
    for(j = ps; j < pe-1; j++)//??????????
        pre[j] = pre[j+1];
    pre[pe-1] = in[i];//????????????????????
    recovery(ps,ps+i-is,is,i);
    recovery(ps+i-is,pe-1,i+1,ie);
}

int main()
{
    while(scanf("%s %s",pre,in) != EOF)
    {
        int len = strlen(pre);
        recovery(0,len,0,len);
        cout << pre << endl;
    }

    return 0;
}
